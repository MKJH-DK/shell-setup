#!/usr/bin/env bash
set -Eeuo pipefail

# index.sh - Main menu for shell-setup scripts
# Provides a unified entry point to select and run setup scripts

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

# ── Colors ────────────────────────────────────────────────
RED='\033[0;31m'; GREEN='\033[0;32m'; YELLOW='\033[1;33m'
CYAN='\033[0;36m'; BOLD='\033[1m'; DIM='\033[2m'; NC='\033[0m'

# ── Load .env API keys ──────────────────────────────────
load_env() {
  local env_file=""
  local search_paths=(
    "$SCRIPT_DIR/.env"
    "$HOME/.env"
    "/storage/emulated/0/vault/03-personal/04-secrets/.env"
    "/storage/emulated/0/vault/.env"
  )
  for p in "${search_paths[@]}"; do
    if [[ -f "$p" ]]; then
      env_file="$p"
      break
    fi
  done

  if [[ -z "$env_file" ]]; then
    return 0
  fi

  local count=0
  while IFS= read -r line || [[ -n "$line" ]]; do
    [[ "$line" =~ ^[[:space:]]*# ]] && continue
    [[ -z "${line// /}" ]] && continue
    if [[ "$line" =~ ^[[:space:]]*([A-Za-z_][A-Za-z0-9_]*)=(.+)$ ]]; then
      local key="${BASH_REMATCH[1]}"
      local val="${BASH_REMATCH[2]}"
      export "$key=$val"
      ((++count))
    fi
  done < "$env_file"

  printf "${GREEN}✓ Loaded %d env vars from %s${NC}\n" "$count" "$env_file"
}

load_env

# ── askall configuration ─────────────────────────────────

ASKALL_CONFIG_DIR="$HOME/.config/askall"
ASKALL_CONFIG="$ASKALL_CONFIG_DIR/config.env"

