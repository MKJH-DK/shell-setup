# Inspired by hive-research registry pattern
declare -A PROVIDER_ENV=(
  [openai]="OPENAI_API_KEY"
  [anthropic]="ANTHROPIC_API_KEY"
  [google]="GOOGLE_API_KEY"
  [openrouter]="OPENROUTER_API_KEY"
  [groq]="GROQ_API_KEY"
  [deepseek]="DEEPSEEK_API_KEY"
  [cerebras]="CEREBRAS_API_KEY"
  [sambanova]="SAMBANOVA_API_KEY"
  [together]="TOGETHER_API_KEY"
  [cohere]="COHERE_API_KEY"
  [siliconflow]="SILICONFLOW_API_KEY"
  [hyperbolic]="HYPERBOLIC_API_KEY"
  [deepinfra]="DEEPINFRA_API_KEY"
  [upstage]="UPSTAGE_API_KEY"
  [venice]="VENICE_API_KEY"
)

# Known models per CLI tool
CLAUDE_MODELS=(
  "claude-sonnet-4-20250514"
  "claude-opus-4-20250514"
  "claude-haiku-4-5-20251001"
  "claude-3-5-sonnet-20241022"
)
GEMINI_MODELS=(
  "gemini-2.5-flash"
  "gemini-2.5-pro"
  "gemini-2.0-flash"
  "gemini-2.0-flash-lite"
)
SGPT_MODELS=(
  "gpt-4o"
  "gpt-4o-mini"
  "gpt-4-turbo"
  "o3-mini"
)

scan_providers() {
  local found=()
  for pid in "${!PROVIDER_ENV[@]}"; do
    local evar="${PROVIDER_ENV[$pid]}"
    local val="${!evar:-}"
    if [[ -n "$val" ]]; then
      found+=("$pid")
    fi
  done
  # Sort for consistent display
  IFS=$'\n' found=($(sort <<< "${found[*]}")); unset IFS
  printf '%s\n' "${found[@]}"
}

askall_load_config() {
  # Returns current config values (or defaults)
  local cfg_tools="" cfg_claude_model="" cfg_gemini_model="" cfg_sgpt_model="" cfg_max=""
  if [[ -f "$ASKALL_CONFIG" ]]; then
    while IFS='=' read -r k v; do
      [[ "$k" =~ ^[[:space:]]*# ]] && continue
      k="${k// /}"; v="${v// /}"
      case "$k" in
        ASKALL_TOOLS) cfg_tools="$v" ;;
        ASKALL_CLAUDE_MODEL) cfg_claude_model="$v" ;;
        ASKALL_GEMINI_MODEL) cfg_gemini_model="$v" ;;
        ASKALL_SGPT_MODEL) cfg_sgpt_model="$v" ;;
        ASKALL_MAX_TOOLS) cfg_max="$v" ;;
      esac
    done < "$ASKALL_CONFIG"
  fi
  echo "${cfg_tools:-auto}"
  echo "${cfg_claude_model:-(default)}"
  echo "${cfg_gemini_model:-gemini-2.5-flash}"
  echo "${cfg_sgpt_model:-(default)}"
  echo "${cfg_max:-0}"
}

askall_save_config() {
  local tools="$1" claude_m="$2" gemini_m="$3" sgpt_m="$4" max_t="$5"
  mkdir -p "$ASKALL_CONFIG_DIR"
  cat > "$ASKALL_CONFIG" <<CFGEOF
# askall configuration — generated by index.sh
# $(date -Iseconds)

# Which CLIs to use (comma-separated). "auto" or empty = detect all installed.
ASKALL_TOOLS=$tools

# Model overrides per CLI (empty = CLI default)
ASKALL_CLAUDE_MODEL=$claude_m
ASKALL_GEMINI_MODEL=$gemini_m
ASKALL_SGPT_MODEL=$sgpt_m

# Max simultaneous CLIs (0 = no limit)
ASKALL_MAX_TOOLS=$max_t
CFGEOF
  printf "${GREEN}✓ Config saved to %s${NC}\n" "$ASKALL_CONFIG"
}

pick_from_list() {
  local title="$1"; shift
  local options=("$@")
  local current="${options[0]}"

  echo ""
  printf "${BOLD}${CYAN}%s${NC}\n" "$title"
  echo "───────────────────────────────────────"
  for i in "${!options[@]}"; do
    printf "  ${BOLD}%d)${NC} %s\n" "$((i + 1))" "${options[$i]}"
  done
  printf "  ${BOLD}c)${NC} ${DIM}Custom input${NC}\n"
  printf "  ${BOLD}k)${NC} ${DIM}Keep current${NC}\n"
  echo ""
  read -rp "$(printf "${BOLD}Select [1-${#options[@]}/c/k]: ${NC}")" choice

  case "$choice" in
    [kK]|"") echo "__KEEP__" ;;
    [cC])
      read -rp "$(printf "${BOLD}Enter model ID: ${NC}")" custom
      echo "$custom"
      ;;
    [1-9]|[1-9][0-9])
      local idx=$((choice - 1))
      if (( idx >= 0 && idx < ${#options[@]} )); then
        echo "${options[$idx]}"
      else
        echo "__KEEP__"
      fi
      ;;
    *) echo "__KEEP__" ;;
  esac
}

configure_askall() {
  clear 2>/dev/null || true
  printf "${BOLD}${CYAN}"
  cat <<'BANNER'
  ┌──────────────────────────────────┐
  │     askall — Configuration       │
  └──────────────────────────────────┘
BANNER
  printf "${NC}\n"

  # ── 1. Scan providers ──────────────────────────────
  printf "${BOLD}Scanning .env for API keys...${NC}\n"
  local providers
  providers="$(scan_providers)"
  if [[ -n "$providers" ]]; then
    printf "${GREEN}Found providers:${NC}\n"
    while IFS= read -r p; do
      printf "  ${GREEN}●${NC} %s ${DIM}(%s)${NC}\n" "$p" "${PROVIDER_ENV[$p]}"
    done <<< "$providers"
  else
    printf "${YELLOW}No API keys found in environment.${NC}\n"
  fi

  # ── 2. Show installed CLIs ─────────────────────────
  echo ""
  printf "${BOLD}Installed CLIs:${NC}\n"
  local installed_clis=()
  for cli in claude gemini sgpt; do
    if command -v "$cli" >/dev/null 2>&1; then
      printf "  ${GREEN}●${NC} %s\n" "$cli"
      installed_clis+=("$cli")
    else
      printf "  ${RED}○${NC} %s ${DIM}(not installed)${NC}\n" "$cli"
    fi
  done

  # ── 3. Load current config ────────────────────────
  local cfg
  cfg="$(askall_load_config)"
  local cur_tools cur_claude cur_gemini cur_sgpt cur_max
  cur_tools="$(sed -n '1p' <<< "$cfg")"
  cur_claude="$(sed -n '2p' <<< "$cfg")"
  cur_gemini="$(sed -n '3p' <<< "$cfg")"
  cur_sgpt="$(sed -n '4p' <<< "$cfg")"
  cur_max="$(sed -n '5p' <<< "$cfg")"

  echo ""
  printf "${BOLD}Current config:${NC}\n"
  printf "  Tools:        %s\n" "$cur_tools"
  printf "  Claude model: %s\n" "$cur_claude"
  printf "  Gemini model: %s\n" "$cur_gemini"
  printf "  Sgpt model:   %s\n" "$cur_sgpt"
  printf "  Max CLIs:     %s\n" "$([ "$cur_max" = "0" ] && echo "no limit" || echo "$cur_max")"

  # ── 4. Select CLIs ────────────────────────────────
  echo ""
  printf "${BOLD}${CYAN}Select CLIs to use${NC}\n"
  echo "───────────────────────────────────────"
  printf "  ${BOLD}1)${NC} All installed (%s)\n" "${installed_clis[*]}"
  printf "  ${BOLD}2)${NC} Claude + Gemini only\n"
  printf "  ${BOLD}3)${NC} Claude only\n"
  printf "  ${BOLD}4)${NC} Gemini only\n"
  printf "  ${BOLD}5)${NC} Sgpt only\n"
  printf "  ${BOLD}c)${NC} ${DIM}Custom (comma-separated)${NC}\n"
  printf "  ${BOLD}k)${NC} ${DIM}Keep current${NC}\n"
  echo ""
  read -rp "$(printf "${BOLD}Select [1-5/c/k]: ${NC}")" cli_choice

  local new_tools="$cur_tools"
  case "$cli_choice" in
    1) new_tools="auto" ;;
    2) new_tools="claude,gemini" ;;
    3) new_tools="claude" ;;
    4) new_tools="gemini" ;;
    5) new_tools="sgpt" ;;
    [cC])
      read -rp "$(printf "${BOLD}CLIs (comma-separated, e.g. claude,gemini): ${NC}")" custom_tools
      [[ -n "$custom_tools" ]] && new_tools="$custom_tools"
      ;;
  esac

  # ── 5. Claude model ───────────────────────────────
  local new_claude="$cur_claude"
  if [[ "$new_tools" == *claude* ]] || [[ "$new_tools" == "auto" ]]; then
    local picked
    picked="$(pick_from_list "Claude model" "${CLAUDE_MODELS[@]}")"
    [[ "$picked" != "__KEEP__" ]] && new_claude="$picked"
  fi

  # ── 6. Gemini model ───────────────────────────────
  local new_gemini="$cur_gemini"
  if [[ "$new_tools" == *gemini* ]] || [[ "$new_tools" == "auto" ]]; then
    local picked
    picked="$(pick_from_list "Gemini model" "${GEMINI_MODELS[@]}")"
    [[ "$picked" != "__KEEP__" ]] && new_gemini="$picked"
  fi

  # ── 7. Sgpt model ─────────────────────────────────
  local new_sgpt="$cur_sgpt"
  if [[ "$new_tools" == *sgpt* ]] || [[ "$new_tools" == "auto" ]]; then
    local picked
    picked="$(pick_from_list "Sgpt/OpenAI model" "${SGPT_MODELS[@]}")"
    [[ "$picked" != "__KEEP__" ]] && new_sgpt="$picked"
  fi

  # ── 8. Max tools ───────────────────────────────────
  echo ""
  printf "${BOLD}${CYAN}Max simultaneous CLIs${NC} ${DIM}(0 = no limit, current: %s)${NC}\n" \
    "$([ "$cur_max" = "0" ] && echo "no limit" || echo "$cur_max")"
  read -rp "$(printf "${BOLD}Enter number [0-9/k=keep]: ${NC}")" max_choice
  local new_max="$cur_max"
  case "$max_choice" in
    [0-9]) new_max="$max_choice" ;;
  esac

  # ── 9. Clean up defaults for saving ────────────────
  [[ "$new_tools" == "auto" ]] && new_tools=""
  [[ "$new_claude" == "(default)" ]] && new_claude=""
  [[ "$new_sgpt" == "(default)" ]] && new_sgpt=""

  # ── 10. Summary & save ────────────────────────────
  echo ""
  printf "${BOLD}Summary:${NC}\n"
  printf "  Tools:        %s\n" "${new_tools:-auto (all installed)}"
  printf "  Claude model: %s\n" "${new_claude:-(CLI default)}"
  printf "  Gemini model: %s\n" "${new_gemini}"
  printf "  Sgpt model:   %s\n" "${new_sgpt:-(CLI default)}"
  printf "  Max CLIs:     %s\n" "$([ "$new_max" = "0" ] && echo "no limit" || echo "$new_max")"
  echo ""
  read -rp "$(printf "${BOLD}Save? [Y/n]: ${NC}")" confirm
  case "$confirm" in
    [nN]) printf "${YELLOW}Cancelled.${NC}\n"; return 0 ;;
  esac

  askall_save_config "$new_tools" "$new_claude" "$new_gemini" "$new_sgpt" "$new_max"
}

